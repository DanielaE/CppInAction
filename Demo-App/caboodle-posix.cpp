constexpr utf8 decode(utf8 state, char codeunit) {
	const auto type = utf8d[codeunit & 0xFF];
	return utf8{ utf8d[256u + state * 16u + type] };
}

constexpr auto sanitized(std::string_view Input) -> std::string {
	std::string Result;
	Result.reserve(Input.size());

	auto State = utf8::accept;
	while (!Input.empty()) {
		uint8_t Length = 0;

		for (auto Char : Input) {
			switch (const auto Next = decode(State, Char)) {
				case accept:
					Result.append(Input, 0, Length + 1);
					State = utf8::accept;
					break;
				case reject:
					Result.append({ '\xEF', '\xBF', '\xBD' }); // 'ï¿½'
					if (State != utf8::accept)
						Length = State / 3u;
					State = utf8::accept;
					break;
				default:
					++Length;
					State = Next;
					break;
			}
			if (State == utf8::accept) {
				Input.remove_prefix(Length + 1);
				break;
			}
		}
	}

	return Result;
}

// filenames have no guaranteed character encoding in POSIX.
// return a sanitized utf-8 encoded string
auto utf8Path(const std::filesystem::path & Path) -> std::string {
	return sanitized(Path.generic_string());
}

